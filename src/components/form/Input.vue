<template>
    <div>
        <input type="text" :value ="value" @input="onInput" v-bind="$attrs">
    </div>
</template>

<script>
    export default {
        inheritAttrs:false,
        name: "Inputs",
        props: {
            value: {
                type: String,
                default: ''
            },
        },
        methods: {
            onInput(e) {
                this.$emit('input',e.target.value)
                this.$parent.$emit('validate')
            }
        },
    }
</script>

